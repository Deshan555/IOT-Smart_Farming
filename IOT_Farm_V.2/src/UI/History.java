    private void jTextField9KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField9KeyReleased
      
        if(jTextField9.getText().length() == 10)
        {
        Connection connection = Connector.connection();
        
        String SQL = "SELECT emp_id, emp_name,  emp_nic,  mobile_number, emp_type FROM employee where emp_id = "+jTextField9.getText()+";";
        
        try
        {
            Statement stmt = connection.createStatement();
            
            ResultSet rs = stmt.executeQuery(SQL);
            
            table.setModel(DbUtils.resultSetToTableModel(rs));
           
        }
        catch(SQLException ERROR)
         {
           Core.Background.Bugs_Log.exceptions(String.valueOf(ERROR));
           
           home Home = new home();
           
           Home.notifications("System Error : SQL Server Offline or System Error", 2);  
         } 
        }
        else if(jTextField9.getText().length() == 0)
        {
            table_load();
        }
        
    }//GEN-LAST:event_jTextField9KeyReleased

    
    public void delete_user()
    {
        String SQL = "DELETE FROM employee WHERE emp_id = "+jTextField6.getText()+";";
        
        Connection connection = Connector.connection();
        
        try
        {
           PreparedStatement preparedStatement = connection.prepareStatement(SQL);

           preparedStatement.executeUpdate();
           
           new NoticeWindow(NoticeType.SUCCESS_NOTIFICATION,"Delete Finished",NoticeWindow.SHORT_DELAY,NPosition.TOP_RIGHT);
           
        }
        catch(SQLException ERROR)
        {
            JOptionPane.showMessageDialog(null,"SQL ERROR : \n"+ERROR);
            
            Core.Background.Bugs_Log.exceptions(String.valueOf(ERROR));
        }
    }
    
    
    public void load_user(String id)
    {
        
        String SQL = "SELECT * FROM employee where emp_id = "+id+";";
        
        Connection connection = Connector.connection();
        
        try
        {
            Statement stmt = connection.createStatement();
            
            ResultSet rs = stmt.executeQuery(SQL);
            
            while(rs.next())
            {
                jTextField6.setText(rs.getString("emp_id"));
                
                jTextField10.setText(rs.getString("emp_name"));
                
                jTextField5.setText(rs.getString("emp_nic"));
                
                jTextField11.setText(rs.getString("mobile_number"));
                
                jComboBox6.setSelectedItem(rs.getString("emp_type"));
                
                jComboBox7.setSelectedItem(rs.getString("gender"));
                
                jComboBox8.setSelectedItem(rs.getString("section"));
                
                jCheckBox6.setSelected(Boolean.valueOf(rs.getString("p1")));
                
                jCheckBox7.setSelected(Boolean.valueOf(rs.getString("p2")));
                
                jCheckBox9.setSelected(Boolean.valueOf(rs.getString("p3")));
                
                jCheckBox8.setSelected(Boolean.valueOf(rs.getString("p4")));
                
                jCheckBox10.setSelected(Boolean.valueOf(rs.getString("p5")));
            }
        }
        catch(Exception ERROR)
        {
            Core.Background.Bugs_Log.exceptions(String.valueOf(ERROR));
        }
        
          tab.setSelectedIndex(2);
          
          
    }
